/* 
Ans:- The comma operator in JavaScript allows multiple expressions to be evaluated in a single statement. It is used to separate multiple expressions where only one expression is expected. The comma operator evaluates each expression from left to right and returns the value of the last expression.

Here's the syntax of the comma operator:

expression1, expression2, expression3, ..., expressionN

The comma operator evaluates each expression and returns the value of the last expression (expressionN). The intermediate expressions (expression1, expression2, etc.) are evaluated for their side effects, such as variable assignments or function calls.

*/